read -p "Enter a Single Digit Number: " x;

if [ $x == 0 ]
then
	echo "$x is Zero"
elif [ $x == 1 ]
then
	echo "$x is One"
elif [ $x == 2 ]
then
	echo "$x is Two"
elif [ $x == 3 ]
then
	echo "$x is Three"
elif [ $x == 4 ]
then
	echo "$x is Four"
elif [ $x == 5 ]
then
	echo "$x is Five"
elif [ $x == 6 ]
then
	echo "$x is Six"
elif [ $x == 7 ]
then
	echo "$x is Seven"
elif [ $x == 8 ]
then
	echo "$x is Eight"
elif [ $x == 9 ]
then
	echo "$x is Nine"
else
	echo "Invalid  Entry"
fi
